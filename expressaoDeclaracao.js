//Declaração: É quando você diz ao computador que algo existe ou que algo deve ser feito. 
//Por exemplo, quando você cria uma função ou define uma variável, você está fazendo uma declaração. 
//É como dar uma ordem para o computador: 
//"Ei, computador, eu quero que você se lembre disso ou faça isso."

//exemplo: 

var x = 0;

function minhaFunção(){
  //código a ser executado
}

// Expressão: É uma combinação de valores, variáveis ​​e operadores que resulta em um valor. 
// Basicamente, é como uma "expressão matemática" que você usa em álgebra. 
// Por exemplo, quando você faz uma operação matemática, você está usando uma expressão.

// Exemplo de expressão:

var soma = 2 + 3;


// A principal diferença entre eles é que uma declaração faz algo (define algo), 
// enquanto uma expressão resulta em um valor.

// Aqui estão algumas regrinhas para lembrar:

// Declarações geralmente terminam com um ponto e vírgula ;.
// É como colocar um ponto final em uma frase. Você está dizendo ao computador que a instrução acabou ali.
// Expressões podem ser parte de uma declaração ou de outra expressão. 
// Por exemplo, você pode usar uma expressão dentro de uma declaração de variável para atribuir um valor
//  a essa variável.